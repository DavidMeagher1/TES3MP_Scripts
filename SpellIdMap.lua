function SpellIdMap.getSpellEffectName(spellid)
  assert(type(spellid) == "number","argument[1] in getSpellEffectName must be a number")
  if SpellIdMap.effectNameMap[spellid] ~= nil then
    return SpellIdMap.effectNameMap[spellid]
  else
    return nil
  end
end

function SpellIdMap.getSpellEffectId(effectName)
  assert(type(effectName) == "string","argument[1] in getSpellEffectId must be a string")
  if SpellIdMap.effectNameMap[effectName] ~= nil then
    return SpellIdMap.effectNameMap[effectName]
  else
    return nil
  end
end

